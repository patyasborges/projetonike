package br.inatel.ec206.model;

public class Marcas 
{
	int ID_marca;
	String Nome_Marca;
	String Imagem_marca;
	public int getID_marca() {
		return ID_marca;
	}
	public void setID_marca(int iD_marca) {
		ID_marca = iD_marca;
	}
	public String getNome_Marca() {
		return Nome_Marca;
	}
	public void setNome_Marca(String nome_Marca) {
		Nome_Marca = nome_Marca;
	}
	public String getImagem_marca() {
		return Imagem_marca;
	}
	public void setImagem_marca(String imagem_marca) {
		Imagem_marca = imagem_marca;
	}
	
	
}
